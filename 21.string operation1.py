name= "HELLO"  #strings are arrays
print(name[0])
print(name[1])
print(name[2])
print(name[:])  #prints entire string
print(name[0:])  #here last index is not given so it is included
print(name[0:2])
print(name[:5])  #here index 5 is after the last index of HELLO so HELLO is printed fully
print(name[:4]) #here last index 4 so only HELL is printed as last index is excluded
print(name[1:4])
print(name[:3]) #last index 3 will be excluded